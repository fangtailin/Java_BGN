package iteration;

public class ForLoop {
    private int x;

    public static void main(String[] args) {
        int i;
        for (i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println(i);

        int j = 0;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
        System.out.println(j);

//        ForLoop forLoop = new ForLoop();
//        System.out.println(forLoop.x);
    }
}
